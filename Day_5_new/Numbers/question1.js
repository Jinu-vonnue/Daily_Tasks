//Round  3.49  and  3.5  using  Math.round ,  Math.floor , and  Math.ceil . Print all results

function round(number) {
  console.log(number + " - round : " + Math.round(number));
  console.log(number + " - floor : " + Math.floor(number));
  console.log(number + " - ceil : " + Math.ceil(number));
}

round(3.49);
round(3.5);
